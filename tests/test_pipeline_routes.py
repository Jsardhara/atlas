def _seed_reply_after_publish(
    redis: FakeRedis,
    *,
    correlation_id: str,
    message_type: str,
    payload: dict | None = None,
) -> threading.Thread:
    """Push a synthetic reply event onto the stream from a side thread."""

    def _worker() -> None:
        # Wait briefly to let the route publish the trigger first.
        time.sleep(0.1)
        envelope = json.dumps(
            {
                "correlation_id": correlation_id,
                "message_type": message_type,
                "payload": payload or {},
            }
        )
        loop = asyncio.new_event_loop()
        try:
            loop.run_until_complete(redis.xadd("atlas:events", {"json": envelope}))
        finally:
            loop.close()

    t = threading.Thread(target=_worker, daemon=True)
    t.start()
    return t


