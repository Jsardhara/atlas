def test_oracle_scan_returns_envelope_on_reply(
    app_and_redis: tuple[FastAPI, FakeRedis],
) -> None:
    app, redis = app_and_redis
    # Pre-publish a reply with a correlation_id we'll observe via the route.
    # Since correlation_id is generated by the route when no signal_id supplied,
    # patch uuid.uuid4 to return a stable id.
    fixed = uuid.UUID("00000000-0000-0000-0000-0000000000aa")
    original_uuid = pipeline_router.uuid.uuid4
    pipeline_router.uuid.uuid4 = lambda: fixed  # type: ignore[assignment]
    try:
        _seed_reply_after_publish(
            redis,
            correlation_id=str(fixed),
            message_type="research_update",
            payload={"signals": []},
        )
        client = TestClient(app)
        res = client.post("/pipeline/oracle-scan", json={"top_n": 5})
        assert res.status_code == 200
        body = res.json()
        assert body["status"] == "ok"
        assert body["correlation_id"] == str(fixed)
        assert body["result"]["message_type"] == "research_update"
    finally:
        pipeline_router.uuid.uuid4 = original_uuid  # type: ignore[assignment]


